    private void findPartIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findPartIDActionPerformed
        // TODO add your handling code here:
        try{
            String pid = tPartID1.getText();
            Participant p2 = new Participant();
            p2.selectDB(pid);
            p2.display();
            fullnameL.setText(p2.getFname() + "\t" + "\t "+ p2.getLname());
             
            Participant p1 = new Participant();
            p1.selectDB(tPartID1.getText());
        
            p1.display();
            StringBuffer str = new StringBuffer();
            for (String element : p1.pg.participantGoalsList) {
            str.append( element + "\n" );
           
            }
            desGTxa.setText(p1.getFname()+" "+p1.getLname()+"\n"+dateFormat.format(cal.getTime()));
            tGoalsArea.setText(str.toString());
        }catch (Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
            
    }//GEN-LAST:event_findPartIDActionPerformed

