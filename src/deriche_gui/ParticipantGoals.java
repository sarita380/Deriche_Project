    private void submitBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBActionPerformed
        // TODO add your handling code here:
        
        ProgressNote pn = new ProgressNote();
        Participant p1 = new Participant(tPartID1.getText());
        String noteid = pn.date+"_"+p1.getFname()+"_"+p1.getLname();
        int dialogButton = 0;
        int dialogResult = JOptionPane.showConfirmDialog (null, "Did "+p1.getFname()+" "+p1.getLname()+" have a siezure?","Warning",dialogButton);
        if(dialogResult == JOptionPane.YES_OPTION){
            desGTxa.append("\nSiezure: YES");
        }
        if(dialogResult == JOptionPane.NO_OPTION){
            desGTxa.append("\nSiezure: NO");
        }
        pn.insertDB(noteid, pn.date, p1.getId(), desGTxa.getText());
        Component frame = null;
        JOptionPane.showMessageDialog(frame, "Note Added");
        //clear();
    }//GEN-LAST:event_submitBActionPerformed

