        StringBuffer str1 = new StringBuffer();
        g.selectDB(ig);
        g.display();
        idgTxf.setText(g.getId());
        descGTxf.setText(g.getDescription());
        
        for(Objective o1:g.go.go){
                str1.append(o1.objectiveDetails()+"\n");
        }
        displaytxa.setText(str1.toString());
        
