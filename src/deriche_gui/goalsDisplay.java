    private void addGoalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addGoalActionPerformed
       
        try{
            String pg = idptxf.getText();
            String ig = idgoalTxf.getText();
            
            Participant p1 =new Participant();
            Goal g1 = new Goal(ig);
            p1.setId(pg);
            p1.addGoal(g1);
            JOptionPane.showMessageDialog(null, "Goal added");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Goal not added");
        }
    }//GEN-LAST:event_addGoalActionPerformed

    private void deleteGoalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteGoalActionPerformed
        // TODO add your handling code here:
        try{
            String pg = idptxf.getText();
            String ig = idgoalTxf.getText();
            
            Participant p1 =new Participant(pg);
            Goal g1 = new Goal(ig);
            p1.getId();
            
            int deleteG = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete? ", "Deleted!", JOptionPane.YES_NO_OPTION);
             if(deleteG == JOptionPane.YES_OPTION){
              //p1.deleteDB();
              p1.delGoal(g1); 
            JOptionPane.showMessageDialog(null, "Successfully Deleted! ");
             }else{
             JOptionPane.showMessageDialog(null, "Goal not Deleted! ");
             }   
        }catch(Exception e){
            
             JOptionPane.showMessageDialog(null, "Goal not ddleted!");
        }
        
    }//GEN-LAST:event_deleteGoalActionPerformed

